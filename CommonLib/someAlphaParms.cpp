// Constructor.

AlphaParms::AlphaParms()
{
   reset();
}

void AlphaParms::reset()
{
   BaseClass::reset();
   if (Ris::portableIsWindows())
   {
      BaseClass::setFilePath("c:/aaa_prime/files/Alpha_Parms.txt");
   }
   else
   {
      BaseClass::setFilePath("/opt/prime/files/Alpha_Parms.txt");
   }

   mN1 = 99;
   mN2 = 99;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Show.

void AlphaParms::show()
{
   printf("\n");
   printf("AlphaParms************************************************ %s\n", mTargetSection);

   printf("\n");
   printf("N1                         %-12d\n", mN1);
   printf("N2                         %-12d\n", mN2);
   printf("\n");
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Base class override: Execute a command from the command file to set a 
// member variable.  Only process commands for the target section.This is
// called by the associated command file object for each command in the file.

void AlphaParms::execute(Ris::CmdLineCmd* aCmd)
{
   if (!isTargetSection(aCmd)) return;

   if (aCmd->isCmd("N1"))               mN1 = aCmd->argInt(1);
   if (aCmd->isCmd("N2"))               mN2 = aCmd->argInt(1);
}

