//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constructor

KTest::KTest()
   :
   mRandomGen(mRandomDevice()),
   mRandomDigitIndex(0, 9),
   mRandomAlphaIndex(0, 25)
{
   reset();
}

void KTest::reset()
{
   mOutString[0] = 0;
   mOutSize = 8;
}

void KTest::show()
{
   puts(mOutString);
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Generate output.

void KTest::doGenOut1()
{
   for (int i = 0; i < mOutSize; i++)
   {
      int k = mRandomDigitIndex(mRandomGen);
      mOutString[i] = cDigitMap[k];
      //Prn::print(0, "some %d %d $ %d", i, k, mOutString[i]);
   }
   mOutString[mOutSize] = 0;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Generate output.

void KTest::doGenOut2()
{
   for (int i = 0; i < mOutSize; i++)
   {
      int k = mRandomAlphaIndex(mRandomGen);
      mOutString[i] = cLowerMap[k];
      //Prn::print(0, "some %d %d $ %d", i, k, mOutString[i]);
   }
   mOutString[mOutSize] = 0;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Run test.

bool KTest::doTest()
{
   puts(mOutString);
   int tIndex = 0;
   while (true)
   {
      int tChar = _getch();
      if (tChar == 27) return false;
      if (tChar == mOutString[tIndex])
      {
         putchar(tChar);
         if (++tIndex == mOutSize) break;
      }
   }
   return true;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Run test.

void KTest::doRun1()
{
   while (true)
   {
      doGenOut1();
      if (!doTest())
      {
         printf("done\n");
         return;
      }
      printf("\n");
   }
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Run test.

void KTest::doRun2()
{
   while (true)
   {
      doGenOut2();
      if (!doTest())
      {
         printf("done\n");
         return;
      }
      printf("\n");
   }
}
